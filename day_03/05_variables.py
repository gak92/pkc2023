# Variables
# 1- Should contain letters, numbers and underscore
# 2- Do not start with numbers
# 3- Spaces not allowed
# 4- Do not use keywords
# 5- Use descriptive
# 6- Case sensitive

x = 5
print(x)

y = "Learning python"
print(y)

x = x + 5
print(x)

print(type(x))
print(type(y))

fruit_basket = 8
print(fruit_basket)
print(type(fruit_basket))

fruit_basket = "Mangoes"
print(fruit_basket)
print(type(fruit_basket))

del fruit_basket
print(fruit_basket)   # NameError: name 'fruit_basket' is not defined
